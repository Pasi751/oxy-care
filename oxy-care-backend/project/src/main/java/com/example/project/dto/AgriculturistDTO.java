package com.example.project.dto;

public class AgriculturistDTO {
	
	private String id;
	private String username;
	private String email;
	private String city;
	private String province;
	private String phoneNumber;
	private String typesOfTreesPlanted;
	private String treesPlanted;
	
	
	public AgriculturistDTO() {}


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}


	public String getUsername() {
		return username;
	}


	public void setUsername(String username) {
		this.username = username;
	}


	public String getEmail() {
		return email;
	}


	public void setEmail(String email) {
		this.email = email;
	}


	public String getCity() {
		return city;
	}


	public void setCity(String city) {
		this.city = city;
	}


	public String getProvince() {
		return province;
	}


	public void setProvince(String province) {
		this.province = province;
	}


	public String getPhoneNumber() {
		return phoneNumber;
	}


	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}


	public String getTypesOfTreesPlanted() {
		return typesOfTreesPlanted;
	}


	public void setTypesOfTreesPlanted(String typesOfTreesPlanted) {
		this.typesOfTreesPlanted = typesOfTreesPlanted;
	}


	public String getTreesPlanted() {
		return treesPlanted;
	}


	public void setTreesPlanted(String treesPlanted) {
		this.treesPlanted = treesPlanted;
	}
	
	
	

}
