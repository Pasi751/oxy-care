package com.example.project.dto;

public class IndustrialistRequestDto {
	
	
	private String id;
	private String proposalTitle;
    private String companyName;
    private String industryType;
    private String estimatedCarbonEmission;
    private String rewardOffered;
    private String username;
    private String contactNumber;
    private String status;
    
    
    public IndustrialistRequestDto() {}


	public String getProposalTitle() {
		return proposalTitle;
	}


	public void setProposalTitle(String proposalTitle) {
		this.proposalTitle = proposalTitle;
	}


	public String getCompanyName() {
		return companyName;
	}


	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}


	public String getIndustryType() {
		return industryType;
	}


	public void setIndustryType(String industryType) {
		this.industryType = industryType;
	}


	public String getEstimatedCarbonEmission() {
		return estimatedCarbonEmission;
	}


	public void setEstimatedCarbonEmission(String estimatedCarbonEmission) {
		this.estimatedCarbonEmission = estimatedCarbonEmission;
	}


	public String getRewardOffered() {
		return rewardOffered;
	}


	public void setRewardOffered(String rewardOffered) {
		this.rewardOffered = rewardOffered;
	}


	public String getUsername() {
		return username;
	}


	public void setUsername(String username) {
		this.username = username;
	}


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}


	public String getContactNumber() {
		return contactNumber;
	}


	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}


	public String getStatus() {
		return status;
	}


	public void setStatus(String status) {
		this.status = status;
	}
	
	
    
	
    
    

}
